#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	void *s0;

	s0 = s;
	while (n)
	{
		n--;
		*(unsigned char *)s = (unsigned char)c;
		s++;
	}
	return (s0);
}
