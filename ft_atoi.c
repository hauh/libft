int		ft_atoi(char const *s)
{
	short	sign;
	long	n;

	while (((*s >= 9) && (*s <= 13)) || (*s == 32))
		s++;
	sign = (*s == '-') ? -1 : 1;
	if ((*s == '-') || (*s == '+'))
		s++;
	n = 0;
	while ((*s >= '0') && (*s <= '9'))
	{
		if (((n >= 922337203685477581) && (*s >= '0') && (*s <= '9')) ||
			((n == 922337203685477580) && (*s >= '8') && (*s <= '9')))
		{
			n = (sign == 1) ? -1 : 0;
			return (n);
		}
		n = n * 10 + (*s - '0');
		s++;
	}
	return (n * sign);
}
