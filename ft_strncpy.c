#include "libft.h"

char	*ft_strncpy(char *d, const char *s, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (s[i])
		{
			d[i] = s[i];
			i++;
		}
		else
			while (i < n)
			{
				d[i] = '\0';
				i++;
			}
	}
	return (d);
}
